#include "ft_printf.h"

int	ft_printf(const char *str, ...)
{
	va_list	args;
	int		i;
	int		length;

	i = 0;
	length = 0;
	va_start(args, str);
	while (str[i])
	{
		if (str[i] == '%')
			length += ft_check_type(args, str[++i]);
		else
			length += ft_write(str[i]);
		i++;
	}
	va_end(args);
	return (length);
}
